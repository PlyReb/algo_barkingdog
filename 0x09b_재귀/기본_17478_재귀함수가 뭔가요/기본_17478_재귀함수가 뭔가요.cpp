#include <bits/stdc++.h> 
using namespace std;

int n;

void sentence(const char* str, int i)//"____"를 i번 찍고 문장 출력하는 함수
{
	for (int j = 0; j < i; ++j)
		cout << "____";
	cout << str;
}

void paragraph(int cnt)
{
	sentence("\"재귀함수가 뭔가요?\"\n", cnt);//base_condition 이나 아닐 때나 늘 나온다.
	if (cnt == n)
		sentence("\"재귀함수는 자기 자신을 호출하는 함수라네\"\n", cnt);
	else
	{
		sentence("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n", cnt);
		sentence("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n", cnt);
		sentence("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n", cnt);
		paragraph(cnt + 1);
	}
	sentence("라고 답변하였지.\n", cnt);//절차적 사고가 아니고 어떻게 해결??
	return;
}

int main(void)
{
  ios::sync_with_stdio(0);
  cin.tie(0);

  cin >> n;
  cout << "어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n";//한번만 나옴
  paragraph(0);
}
