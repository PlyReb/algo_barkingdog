#include<bits/stdc++.h>
using namespace std;

int n;
long long arr[1000001];

int main(void)
{
	ios::sync_with_stdio(0);
	cin.tie(0);

	cin >> n;
	for (int i = 0; i < n; i++)
		cin >> arr[i];
	sort(arr, arr + n);//arr 주소 부터 arr+n-1 주소까지의 값 정렬.

	int cnt = 0;
    long long max_val = -(1ll << 62) - 1; // 1을 long long으로 형변환하지 않고 1 << 62로 작성시 int overflow 발생
    int max_cnt = 0;

    for (int i = 0; i < n; i++)
    {
        if (i == 0 || arr[i - 1] == arr[i])
            cnt++; // i가 0인 경우 앞쪽 식이 true이기 때문에 a[i-1]을 참조하지 않음
        else
        {
            if (cnt > max_cnt)
            {
                max_cnt = cnt;
                max_val = arr[i - 1];
            }
            cnt = 1;
        }
    }
    if (cnt > max_cnt)
        max_val = arr[n - 1]; // 제일 마지막 수가 몇 번 등장했는지를 별도로 확인
    cout << max_val;
}